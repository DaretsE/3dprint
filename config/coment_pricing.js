//   расход_факт = расход_клиента × (1 + materialOverhead.fdmPercent / 100)
//   (если надбавка = 0, расход_факт = расход_клиента)
//
// ШАГ 1 — Стоимость материала одного стола:
//   FDM:   (расход_факт г  / 1000) × pricePerKg
//   Смола: (расход_факт мл / 1000) × pricePerLiter
//
// ШАГ 2 — Стоимость машинного времени одного стола:
//   время_мин × costPerMinute
//
// ШАГ 3 — Электроэнергия одного стола:
//   (мощность_Вт × время_ч / 1000) × electricityPricePerKwh
//
// ШАГ 4 — Амортизация оборудования одного стола:
//   время_ч × amortizationHourlyRate
//
// ШАГ 5 — Доля постоянных затрат одного стола:
//   время_мин × (lease + other) / (fdmPrinterCount × targetDailyOperatingHours × 30 × 60)
//
// ШАГ 6 — Расходники одного стола:
//   время_ч × consumablesPerHour
//
// ШАГ 7 — Себестоимость одного стола:
//   шаг1 + шаг2 + шаг3 + шаг4 + шаг5 + шаг6
//
// ШАГ 8 — Цена одного стола с наценкой:
//   себестоимость × (1 + operatorMarkupPercent / 100)
//
// ШАГ 9 — Суммарная стоимость всех столов (до скидки):
//   цена_стола × количество_столов
//
// ШАГ 10 — Скидка за тираж:
//   всего_изделий  = количество_столов × количество_изделий_на_столе
