//   сумма_скидки   = суммарная_стоимость × скидка_% / 100
//
// ШАГ 11 — ИТОГОВАЯ СТОИМОСТЬ ЗАКАЗА:
//   итого = суммарная_стоимость − сумма_скидки
//
// ШАГ 12 — Стоимость одного изделия:
//   итого / всего_изделий
