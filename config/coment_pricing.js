// 📊 ФОРМУЛА РАСЧЁТА — как работает калькулятор шаг за шагом
// ═══════════════════════════════════════════════════════════════════════════
//
// ВХОДНЫЕ ДАННЫЕ (вводит клиент):
//   • Время печати стола       — часы и минуты одного цикла печати
//   • Расход материала на стол — граммы (FDM) или миллилитры (смола)
//   • Количество столов        — сколько раз запускается цикл печати
//   • Количество изделий на столе — сколько одинаковых изделий за 1 цикл
//
